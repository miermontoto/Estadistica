> Test de independencia Chi-Cuadrado: estudia la relación genérica -> cualitativas
> Test de correlación de Pearson: si hay o no relación lineal -> cuantitativas

> 1. Elegir el contraste/prueba
> 2. Establecer hipótesis
H0: no existe relación entre las variables
H1: existe relación
> 3. Interpretar el p-valor
# --- #
Chi-Cuadrdado:
H0: hay independencia estadística entre las variables
H1: no hay independencia estadística
Correlación de Pearson:
H0: hay independencia / no hay relación lineal / rho == 0
H1: hay dependencia / hay relación lineal / rho != 0

