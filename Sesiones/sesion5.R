# Pearson
> Coeficiente de correlación de Pearson, rho
> Toma valores entre -1 y 1
> Cuantifica la relación lineal que hay y el sentido
> Una relación positiva -> proporcionales
> Una relación negativa -> proporcionalmente inversas

> Cuando toma el valor 0, no hay relación (correlación nula)
> Cuando toma el valor 1 o -1, es una línea recta perfecta (relación lineal perfecta +-)
> Cuando toma un valor en (0.9, 1) -> correlación positiva muy alta
> Cuando toma un valor en (0.7, 0.9) -> correlación positiva alta
> Cuando toma un valor en (0.4, 0.7) -> correlación positiva moderada
> Cuando toma un valor en (0.2, 0.4) -> correlación positiva baja
> Cuando toma un valor en (0, 0.2) -> correlación positiva muy baja

H0: r=0 (no hay relación lineal)
H1: r!=0 (hay relación lineal)
